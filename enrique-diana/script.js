    function initAmbientParticles() {
        if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) return;

        const canvas = document.createElement('canvas');
        canvas.id = 'ambient-canvas';
        canvas.style.position = 'fixed';
        canvas.style.top = '0';
        canvas.style.left = '0';
        canvas.style.width = '100vw';
        canvas.style.height = '100vh';
        canvas.style.pointerEvents = 'none';
        canvas.style.zIndex = '0'; // Behind everything
        document.body.appendChild(canvas);

        const ctx = canvas.getContext('2d');
        let width = canvas.width = window.innerWidth;
        let height = canvas.height = window.innerHeight;

        window.addEventListener('resize', () => {
            width = canvas.width = window.innerWidth;
            height = canvas.height = window.innerHeight;
        }, { passive: true });

        const particles = [];
        // Two colors: primary olive (#54582f) and blush rose (#DFBDB3)
        const colors = [
            { r: 84, g: 88, b: 47, opacity: 0.12, type: 'leaf' },
            { r: 223, g: 189, b: 179, opacity: 0.18, type: 'petal' }
        ];

        class Particle {
            constructor() {
                this.reset();
                this.y = Math.random() * height; // Start at random height initially
            }

            reset() {
                this.x = Math.random() * width;
                this.y = -20;
                this.size = Math.random() * 8 + 6;
                this.speedY = Math.random() * 0.6 + 0.4;
                this.speedX = Math.random() * 0.4 - 0.2;
                const baseColor = colors[Math.floor(Math.random() * colors.length)];
                this.r = baseColor.r;
                this.g = baseColor.g;
                this.b = baseColor.b;
                this.opacity = baseColor.opacity;
                this.type = baseColor.type;
                this.angle = Math.random() * Math.PI * 2;
                this.spinSpeed = Math.random() * 0.02 - 0.01;
            }

            update() {
                this.y += this.speedY;
                this.x += this.speedX + Math.sin(this.y * 0.01) * 0.15; // gentle wave drift
                this.angle += this.spinSpeed;

                if (this.y > height + 20 || this.x < -20 || this.x > width + 20) {
                    this.reset();
                }
            }

            draw() {
                ctx.save();
                ctx.translate(this.x, this.y);
                ctx.rotate(this.angle);
                ctx.fillStyle = `rgba(${this.r}, ${this.g}, ${this.b}, ${this.opacity})`;
                ctx.beginPath();
                if (this.type === 'leaf') {
                    // Draw a simple leaf shape (two quadratic curves)
                    ctx.moveTo(0, -this.size);
                    ctx.quadraticCurveTo(this.size * 0.6, 0, 0, this.size);
                    ctx.quadraticCurveTo(-this.size * 0.6, 0, 0, -this.size);
                } else {
                    // Draw a soft rounded flower petal shape
                    ctx.moveTo(0, 0);
                    ctx.arc(0, 0, this.size * 0.5, 0, Math.PI * 2);
                }
                ctx.fill();
                ctx.restore();
            }
        }

        const maxParticles = Math.min(25, Math.floor((width * height) / 40000));
        for (let i = 0; i < maxParticles; i++) {
            particles.push(new Particle());
        }

        function animate() {
            ctx.clearRect(0, 0, width, height);
            for (let i = 0; i < particles.length; i++) {
                particles[i].update();
                particles[i].draw();
            }
            requestAnimationFrame(animate);
        }

        animate();
    }

